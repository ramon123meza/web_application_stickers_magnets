// Get price for a specific product configuration
export function getPrice(productType, size, quantity) {
  let priceTable;

  switch (productType) {
    case PRODUCT_TYPES.STICKERS:
      priceTable = STICKER_PRICES;
      break;
    case PRODUCT_TYPES.DIE_CUT_MAGNETS:
      priceTable = MAGNET_PRICES;
      break;
    case PRODUCT_TYPES.FRIDGE_MAGNETS:
      priceTable = FRIDGE_MAGNET_PRICES;
      break;
    default:
      return null;
  }

  if (!priceTable[size] || !priceTable[size][quantity]) {
    return null;
  }

  return priceTable[size][quantity];
}

// Get per-unit price
export function getPerUnitPrice(productType, size, quantity) {
  const totalPrice = getPrice(productType, size, quantity);
  if (!totalPrice) return null;
  return totalPrice / quantity;
}

// Calculate savings percentage compared to smallest quantity
export function getSavingsPercentage(productType, size, quantity) {
  const currentPerUnit = getPerUnitPrice(productType, size, quantity);
  const basePerUnit = getPerUnitPrice(productType, size, 12);

  if (!currentPerUnit || !basePerUnit || quantity === 12) return 0;

  const savings = ((basePerUnit - currentPerUnit) / basePerUnit) * 100;
  return Math.round(savings);
}

// Get size dimensions from size string (e.g., '5x5' -> { width: 5, height: 5 })
export function parseSizeDimensions(sizeString) {
  const parts = sizeString.split('x');
  return {
    width: parseFloat(parts[0]),
    height: parseFloat(parts[1])
  };
}

